//Gray palette.
void
LoadGrayPalette (BYTE data[][3],
				 int size = 256,
				 int redFactor = 1, int greenFactor = 1, int blueFactor = 1)
{
	int i = 0;
	for (i = 0; i < size; i++)
	{
		data[i][0] = redFactor * i;
		data[i][1] = greenFactor * i;
		data[i][2] = blueFactor * i;
	}
}


//Load Hot palette.
void
LoadHotPalette (BYTE data[][3], int size = 256)
{

	int i = 0;
	int oneThird = size / 3;
	int twoThird = 2 * size / 3;
	double dR = 255.00 / (double) (oneThird - 1);

	for (i = 0; i < size; i++)
	{
		double R = dR * i;
		data[i][0] = (BYTE)MIN (255, R);
	}

	double dG = 255.00 / (twoThird - oneThird);
	for (i = 0; i < oneThird; i++)
	{
		data[i][1] = (BYTE)0;
	}

	for (; i < twoThird; i++)
	{
		double G = dG * (i - oneThird);
		data[i][1] = (BYTE)MIN (255, G);
	}
	for (; i < size; i++)
	{
		data[i][1] = (BYTE)255;
	}

	double dB = 255.00 / (size - twoThird);
	for (i = 0; i < twoThird; i++)
		data[i][2] = 0;
	for (; i < size; i++)
		data[i][2] = (BYTE)dB * (i - twoThird);
}

//Load cool palette.
void
LoadCoolPalette (BYTE data[][3], int size)
{

	double dR = 255.00 / (double) (size - 1);
	int i = 0;

	for (i = 0; i < size; i++)
	{
		data[i][0] = (BYTE)(i * dR);
		data[i][1] = (BYTE)((size - 1 - i) * dR);
		data[i][2] = (BYTE)255;
	}

}

//Load RGB Palette.
void
LoadRGBPalette (BYTE data[][3], int size)
{
	int windowSize = 2 * size / 3;
	double stepSize = 255.00 / (double) (1.2 * (windowSize - 1));
	int oneThird = size / 3;
	int i = 0;
	int last = 0;

	for (i = 0; i < windowSize / 2; i++)
		data[i][2] = (BYTE)(stepSize * i);
	last = i;
	for (; i < windowSize; i++)
		data[i][2] = (BYTE)(255 - (i - last) * stepSize);
	for (; i < size; i++)
		data[i][2] = (BYTE)0;

	for (i = 0; i < oneThird; i++)
		data[i][1] = (BYTE)0;
	last = i;
	for (; i < (last + windowSize / 2); i++)
		data[i % size][1] = (BYTE)((i - last) * stepSize);
	last = i;
	for (; i < (last + windowSize / 2); i++)
		data[i % size][1] = (BYTE)(255 - (i - last) * stepSize);
	for (; i < size; i++)
		data[i][1] = 0;

	for (i = 0; i < 2 * oneThird; i++)
		data[i][0] = 0;
	last = i;
	for (; i < (last + windowSize / 2); i++)
		data[i % size][0] = (BYTE)(stepSize * (i - last));
	last = i;
	for (; i < last + windowSize / 2; i++)
		data[i % size][0] = (BYTE)(255 - stepSize * (i - last));
}

//Load jet-palette.
void
LoadJetPalette (BYTE data[][3], int size)
{
	int windowSize = size / 2;
	double stepSize = 255.00 / (double) (1.2 * (windowSize - 1));
	int oneThird = size / 3;
	int i = 0;
	int last = 0;

	for (i = 0; i < windowSize; i++)
		data[i][2] = (BYTE)(i * stepSize);
	for (; i < size; i++)
		data[i][2] = (BYTE)0;

	for (i = 0; i < oneThird; i++)
		data[i][1] = 0;
	last = i;
	for (; i < (last + windowSize); i++)
		data[i % size][1] = (BYTE)((i - last) * stepSize);
	for (; i < size; i++)
		data[i][1] = (BYTE)0;

	for (i = 0; i < 2 * oneThird; i++)
		data[i][0] = 0;
	last = i;
	for (; i < (last + windowSize); i++)
		data[i % size][0] = (BYTE)(stepSize * (i - last));
}





//Load AppropriatePalette.

void
LoadSelectedPalette (BYTE data[][3], int size, GLColorScheme glColorScheme)
{
	switch (glColorScheme)
	{
	case GLColorSchemeScaledGray:
		LoadGrayPalette (data, size);
		break;

	case GLColorSchemeScaledRed:
		LoadGrayPalette (data, size, 1, 0, 0);
		break;

	case GLColorSchemeScaledGreen:
		LoadGrayPalette (data, size, 0, 1, 0);
		break;

	case GLColorSchemeScaledBlue:
		LoadGrayPalette (data, size, 0, 0, 1);
		break;

	case GLColorSchemeHot:
		LoadHotPalette (data, size);
		break;

	case GLColorSchemeCool:
		LoadCoolPalette (data, size);
		break;

	case GLColorSchemeJet:
		LoadJetPalette (data, size);
		break;

	case GLColorSchemeHSV:
		LoadHSVPalette (data);
		break;

	case GLColorSchemeStandard:
		LoadStandardPalette (data);
		break;

	case GLColorSchemeGrayBlue:
		LoadGrayBluePalette (data);
		break;

	case GLColorSchemeGrayGreen:
		LoadGrayGreenPalette (data);
		break;

	case GLColorSchemePastel:
		LoadPastelPalette (data);
		break;

	case GLColorSchemeArmy:
		LoadArmyPalette (data);
		break;

	case GLColorSchemeElevation:
		LoadElevationPalette (data);
		break;

	case GLColorSchemeThresholdedPalette:
		LoadThresholdedPalette (data);
		break;

	default:
		LoadGrayPalette (data, size);
		break;
	}
}

