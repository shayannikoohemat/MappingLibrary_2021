void
LoadThresholdedPalette (BYTE pData[][3])
{
#define THRESHOLDING_LEVEL_COUNT	15
	//Select 15 different colors to fill the thresholded palette.
	BYTE pThresholdingColors[THRESHOLDING_LEVEL_COUNT + 1][3] = {	//gray
		{0, 0, 0}, {128, 128, 128}, {216, 218, 203},
		//blue
		{128, 0, 0}, {255, 0, 0}, {237, 214, 80},
		//green
		{0, 128, 0}, {0, 150, 150}, {0, 255, 0},
		//red
		{0, 0, 128}, {0, 0, 255}, {255, 0, 255},
		//others
		{255, 170, 255}, {0, 255, 255}, {255, 255, 255}, {255, 255, 255}
	};
	//Now load these selected colors in our palette array.
	for (int i = 0; i < PALETTE_ENTRIES_COUNT; i++)
	{
		pData[i][0] =
			pThresholdingColors[(int)
								(i /
								 ((double) PALETTE_ENTRIES_COUNT /
								  (double) (THRESHOLDING_LEVEL_COUNT)))][0];
		pData[i][1] =
			pThresholdingColors[(int)
								(i /
								 ((double) PALETTE_ENTRIES_COUNT /
								  (double) (THRESHOLDING_LEVEL_COUNT)))][1];
		pData[i][2] =
			pThresholdingColors[(int)
								(i /
								 ((double) PALETTE_ENTRIES_COUNT /
								  (double) (THRESHOLDING_LEVEL_COUNT)))][2];
	}
}
